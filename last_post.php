        <h3>17.&nbsp;grudnia 2023&nbsp;r.</h3>
        <h3>III&nbsp;Niedziela Adwentu, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Zachęcamy parafian do&nbsp;uczestnictwa w&nbsp;Mszach św.
        Roratach i&nbsp;do&nbsp;spotkania z&nbsp;przebaczającym Jezusem
        w&nbsp;Sakramencie Pokuty – <strong>Spowiedź&nbsp;św. adwentowa
        w&nbsp;naszej parafii będzie w&nbsp;najbliższy czwartek
        w&nbsp;godzinach od&nbsp;10.00 do&nbsp;11.00 oraz od&nbsp;16.30
        do&nbsp;18.00.</strong> Msza&nbsp;św. Roraty w&nbsp;naszym kościele,
        w&nbsp;dni powszednie, w&nbsp;okresie Adwentu o&nbsp;godz.&nbsp;6.30.
        W&nbsp;tym tygodniu spowiedzi parafialne odbędą się: we&nbsp;wtorek
        u&nbsp;OO.&nbsp;Pallotynów od&nbsp;17.00, w&nbsp;środę parafii:
        św.&nbsp;Pawła od&nbsp;9.30, w&nbsp;piątek w&nbsp;katedrze
        od&nbsp;15.30, a&nbsp;w&nbsp;sobotę w&nbsp;parafii św.&nbsp;Józefa
        od&nbsp;godz.&nbsp;9.00. Prosimy skorzystać ze&nbsp;spowiedzi
        adwentowej. W&nbsp;naszej parafii pół godziny przed Mszą&nbsp;św.
        wieczorną będą księża w&nbsp;konfesjonale przez wszystkie dni robocze
        tego tygodnia. Prosimy nie odkładać spowiedzi na&nbsp;przyszłą
        niedzielę. Dokładny program spowiedzi adwentowej w&nbsp;sandomierskich
        parafiach w&nbsp;parafialnej gablocie.</p>
        <p>2. Można nabyć świece wigilijną „Caritas” – Wigilijne Dzieło Pomocy
        Dzieciom - jak to&nbsp;było w&nbsp;poprzednich latach. Mała świeca -
        10&nbsp;zł, duża - 25&nbsp;zł. Świece są&nbsp;w&nbsp;zakrystii.
        Poświęcone Opłatki wigilijne też można nabyć w&nbsp;zakrystii.
        Zapraszamy.</p>
        <p>3. Fundacja Małych Stópek, zaprasza do&nbsp;udziału
        w&nbsp;świątecznej zbiórce artykułów higienicznych „Paczuszka dla
        Maluszka”, dla dzieci w&nbsp;domach samotnej matki, domach dziecka
        i&nbsp;hospicjach w&nbsp;całej Polsce. W&nbsp;inicjatywę można włączyć
        się przygotowując paczkę z&nbsp;artykułami takimi jak: kremy,
        szampony, mydła, żele do&nbsp;kąpieli, proszki i&nbsp;płyny
        do&nbsp;prania oraz art. chemii gospodarczej i&nbsp;zostawiając
        je&nbsp;pod choinką przy kaplicy MB w&nbsp;naszym kościele.</p>
        <p>4. W&nbsp;zeszłą niedzielę po&nbsp;Mszach&nbsp;św. do&nbsp;puszek
        na&nbsp;wsparcie Kościoła na&nbsp;Wschodzie zostało zebrane
        2043&nbsp;zł. Pieniądze zostały przekazane na&nbsp;wspomniany cel.
        <em>Bóg zapłać</em> wszystkim ofiarodawcom.</p>
        <p>5. Spotkanie organizacyjne dla wszystkich osób, które chcą włączyć
        się do&nbsp;grupy „Różaniec rodziców za&nbsp;dzieci” odbędzie się
        w&nbsp;czwartek - <strong>21 grudnia</strong> - po&nbsp;Mszy świętej
        wieczorowej. Prosimy, aby na&nbsp;to&nbsp;spotkanie przybyły zarówno
        osoby, które już się zapisały, jak i&nbsp;wszystkie chętne osoby.</p>
        <p>6. W&nbsp;najbliższy piątek, tj.&nbsp;22&nbsp;grudnia br.
        od&nbsp;godz.&nbsp;9.00 odwiedzimy tych chorych z&nbsp;sakramentalną
        posługą, którzy zostaną zgłoszeni w&nbsp;zakrystii lub
        w&nbsp;parafialnej kancelarii.</p>
        <p>7. Spotkanie wszystkich ministrantów, lektorów oraz kandydatów
        na&nbsp;ministrantów w&nbsp;sobotę - <strong>23 grudnia</strong>
        o&nbsp;godz.&nbsp;10.00 w&nbsp;kościele.</p>
        <p>8. Zachęcamy do&nbsp;czytania prasy katolickiej; „Gość Niedzielny”,
        „Niedziela”, są&nbsp;do&nbsp;nabycia na&nbsp;stoliku pod chórem
        w&nbsp;kościele.</p>
        <p>9. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, złożone ofiary na&nbsp;rzecz parafii.
        Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
