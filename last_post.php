<div class="card">
    <div class="card-header">
        <h3>19.&nbsp;marca 2023&nbsp;r.</h3>
        <h3>IV&nbsp;Niedziela Wielkiego Postu, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. <strong>Rozpoczęliśmy Parafialne Rekolekcje
        Wielkopostne</strong> w&nbsp;naszej parafii. Jutro
        i&nbsp;we&nbsp;wtorek o&nbsp;godz.&nbsp;18.00 Msza&nbsp;św.
        z&nbsp;nauką rekolekcyjną. Dzieci i&nbsp;młodzież z&nbsp;naszej
        parafii uczące się w&nbsp;Szkole Podstawowej nr&nbsp;4, rozpoczną
        rekolekcje wielkopostne w&nbsp;przyszłym tygodniu. Program rekolekcji
        wcześniej wspomnianych dzieci i&nbsp;młodzieży podamy w&nbsp;przyszłą
        niedzielę.<br>Parafialna Spowiedź wielkopostna w&nbsp;przyszłym tygodniu,
        we&nbsp;wtorek po&nbsp;V&nbsp;niedzieli Wielkiego Postu,
        tj.&nbsp;28&nbsp;marca&nbsp;br.</p>
        <p>2. W&nbsp;Uroczystość Zwiastowania Pańskiego, 25&nbsp;marca,
        kościół w&nbsp;Polsce obchodzi dzień Świętości Życia. Jego celem jest
        budzenie wrażliwości na&nbsp;sens i&nbsp;wartość ludzkiego życia,
        na&nbsp;każdym jego etapie oraz zwrócenie uwagi na&nbsp;potrzebę
        szczególnej troski o&nbsp;nie. Wyrazem takiej troski jest
        m.in.&nbsp;podjęcie Duchowej Adopcji dziecka Poczętego,
        <em>to&nbsp;zobowiązanie do&nbsp;codziennego odmawiania jednej
        dziesiątki Różańca&nbsp;św. oraz specjalnej krótkiej modlitwy przez
        dziewięć miesięcy w&nbsp;intencji poczętego dziecka, by&nbsp;przyjęte
        z&nbsp;miłością mogło urodzić się w&nbsp;kochającej rodzinie</em>.
        Zachęcamy do&nbsp;włączenia się w&nbsp;tę&nbsp;inicjatywę. Deklaracje
        Duchowej Adopcji w&nbsp;formie małych książeczek znajdują się
        na&nbsp;stoliku obok prasy katolickiej. Uroczyste złożenie
        przyrzeczenia, które zapoczątkuje duchową Adopcję odbędzie się
        na&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;18.00 w&nbsp;naszym kościele
        parafialnym 25&nbsp;marca, tj.&nbsp;w&nbsp;najbliższą sobotę.
        Msze&nbsp;św. w&nbsp;tym dniu o&nbsp;godz.&nbsp;6.30, 9.00
        i&nbsp;o&nbsp;18.00. Diecezjalne obchody tego dnia odbędą się
        w&nbsp;sandomierskiej katedrze.</p>
        <p>3. W&nbsp;najbliższą sobotę zapraszamy dzieci przygotowujące się
        do&nbsp;I&nbsp;Komunii Świętej. O&nbsp;17.00 spotkanie w&nbsp;salkach
        parafialnych, o&nbsp;18.00 Msza Święta z&nbsp;poświęceniem
        modlitewników.</p>
        <p>4. Również w&nbsp;najbliższą sobotę odbędzie się w&nbsp;naszej
        parafii kolejny Wieczór Uwielbienia. O&nbsp;godzinie 18.00
        Eucharystia, następnie Adoracja Najświętszego Sakramentu.</p>
        <p>5. W&nbsp;dniu 31&nbsp;marca 2023 roku z&nbsp;kościoła
        pw.&nbsp;Ducha&nbsp;Św. w&nbsp;Sandomierzu wyruszy po&nbsp;raz
        kolejny Ekstremalna Droga Krzyżowa. Rozpoczęcie Mszą&nbsp;św.
        o&nbsp;godz.&nbsp;20.00. Więcej informacji na&nbsp;plakacie
        w&nbsp;gablocie. Ekstremalna Droga Krzyżowa to&nbsp;forma duchowości
        dla tych, którzy nie boją się wyzwań, pragnąc doświadczyć obecności
        Chrystusa w&nbsp;ciszy, samotności, zmęczeniu wędrując kilkadziesiąt
        kilometrów. Tegoroczna edycja Ekstremalnej Drogi Krzyżowej odbywa się
        pod hasłem: <em>Idę, bo szukam nadziei</em>.</p>
        <p>6. Do&nbsp;nabycia w&nbsp;zakrystii Baranki wielkanocne
        w&nbsp;cenie 8&nbsp;zł.</p>
        <p>7. Zachęcamy do&nbsp;nabycia i&nbsp;lektury katolickiej prasy.
        Gazety na&nbsp;stoliku pod chórem.</p>
        <p>8. W&nbsp;niedzielę 26&nbsp;marca&nbsp;br. po&nbsp;Mszach&nbsp;św.
        w&nbsp;naszym kościele, będzie przeprowadzona zbiórka pieniędzy
        na&nbsp;rzecz budowy Domu Hospicyjnego w&nbsp;Skowierzynie (parafia
        Zaleszany) dla dzieci chorych na&nbsp;raka. Zbiórkę poprowadzi
        Fundacja „Z&nbsp;Serca dla Serca”, która zajmuje się budową.
        Na&nbsp;styku trzech województw, tj.&nbsp;podkarpackiego,
        świętokrzyskiego i&nbsp;lubelskiego istnieje luka na&nbsp;mapie
        hospicjów, stąd powstał pomysł wybudowania tego typu placówki, która
        będzie świadczyć pomoc medyczną, psychologiczną i&nbsp;duchową dla
        pacjentów oraz ich rodzin, opiekunów. Warto włączyć się
        w&nbsp;to&nbsp;dzieło, gdyż placówka powstaje w&nbsp;naszej diecezji.
        cZ całego serca zapraszamy do wsparcia budowy Domu Hospicyjnego.</p>
        <p>9. Przypominamy, że&nbsp;Akcja Katolicka Diecezji Sandomierskiej
        zaprasza wszystkich wiernych diecezji sandomierskiej do&nbsp;wzięcia
        udziału w&nbsp;inicjatywie modlitewnej „Różaniec o&nbsp;pokój”.
        Polecając miłosiernemu Bogu, przez wstawiennictwo Matki Bożej sprawę
        pokoju na&nbsp;świecie, zachęcamy wszystkich do&nbsp;modlitwy
        różańcowej, a&nbsp;następnie przekazanie omodlonego różańca
        ks.&nbsp;proboszczowi swojej parafii. Na&nbsp;koniec Wielkiego Postu,
        zgromadzone różańce zostaną przesłane do&nbsp;Kijowa, gdzie miejscowe
        siostry będą rozdawać mieszkańcom i&nbsp;zachęcać do&nbsp;jedności
        modlitewnej. Wierzymy mocno, że&nbsp;wspólna modlitwa i&nbsp;gest
        przekazania swojego różańca będzie nie tylko wyrazem solidarności
        z&nbsp;najbardziej potrzebującymi, ale także uproszeniem łask
        u&nbsp;Boga tak bardzo potrzebnych każdemu z&nbsp;nas.</p>
        <p>10. Dziękujemy za&nbsp;modlitwę, wszelką pomoc, składane ofiary
        na&nbsp;potrzeby parafii. Na&nbsp;kwiaty do&nbsp;grobu Pańskiego
        ofiarę w&nbsp;wysokości 200&nbsp;zł złożyła Róża Różańcowa,
        w&nbsp;której Zelatorem jest Pani Barbara Juda. <em>Bóg zapłać</em>.
        Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
