        <h3>21.&nbsp;maja 2023&nbsp;r.</h3>
        <h3>VII&nbsp;Niedziela Wielkanocna, Rok&nbsp;A</h3>
        <h3>Uroczystość Wniebowstąpienia Pańskiego</h3>
    </div>
    <div class="col">
        <p>1. Ksiądz Biskup Ordynariusz Krzysztof Nitkiewicz zachęca Kapłanów,
        Osoby Życia Konsekrowanego oraz wszystkich Wiernych Świeckich, aby
        w&nbsp;trakcie tegorocznej nowenny przed Uroczystością Zesłania Ducha
        Świętego w&nbsp;całej diecezji podjęli modlitwę w&nbsp;intencji
        powołań kapłańskich. Od&nbsp;19 do&nbsp;27 maja w&nbsp;kolejno
        w&nbsp;każdej parafii dekanatu powinna być zorganizowana adoracja
        Najświętszego Sakramentu. Będzie się ona rozpoczynać Koronką
        do&nbsp;Bożego Miłosierdzia o&nbsp;godz.&nbsp;15.00, a&nbsp;kończyć
        odmówieniem Nowenny do&nbsp;Ducha Świętego oraz odśpiewaniem Apelu
        Jasnogórskiego o&nbsp;godz.&nbsp;21.00.<br>W&nbsp;najbliższą sobotę
        od&nbsp;godz.&nbsp;15.00 w&nbsp;naszym kościele parafialnym
        rozpoczniemy adorację Najśw. połączoną z&nbsp;modlitwą w&nbsp;intencji
        powołań i&nbsp;zakończymy o&nbsp;21.00 Apelem Jasnogórskim.
        Serdecznie zapraszamy.</p>
        <p>2. Dziś po&nbsp;Mszach&nbsp;Św. zbiórka do&nbsp;puszek
        na&nbsp;Instytut Teologiczny w&nbsp;Sandomierzu.</p>
        <p>3. W&nbsp;środę po&nbsp;Mszy świętej <strong>o&nbsp;godz.&nbsp;18.30
        odbędzie się spotkanie rodziców</strong> młodzieży, która w&nbsp;tym
        roku przystąpi do&nbsp;sakramentu bierzmowania.</p>
        <p>4. Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Gazety są&nbsp;na&nbsp;stoliku pod chórem.</p>
        <p>5. Jutro o&nbsp;godz.&nbsp;11.00 w&nbsp;naszym kościele
        parafialnym będzie Msza&nbsp;św. w&nbsp;intencji strażaków, którą
        celebrował będzie ks.&nbsp;Biskup Ordynariusz w&nbsp;ramach
        uroczystych obchodów Dnia Strażaka. Zapraszamy parafian
        do&nbsp;wzięcia udziału w&nbsp;tej modlitwie.</p>
        <p>6. Przypominamy, że&nbsp;w&nbsp;piątek, tj.&nbsp;26&nbsp;czerwca
        obchodzimy Dzień Matki, należy się im&nbsp;nasza wielka wdzięczność
        i&nbsp;modlitwa.</p>
        <p>7. Zapraszamy na&nbsp;kolejny <strong>Wieczór Uwielbienia</strong>,
        który odbędzie się w&nbsp;naszym kościele, w&nbsp;najbliższą sobotę.
        Rozpoczęcie Mszą&nbsp;św. o&nbsp;godz.&nbsp;18.00, a&nbsp;następnie
        adoracja i&nbsp;uwielbienie Pana Jezusa w&nbsp;Najśw. Sakramencie.</p>
        <p>8. W&nbsp;przyszłą niedzielę Uroczystość Zesłania Ducha&nbsp;Św.
        – zakończenie okresu wielkanocnego.</p>
        <p>9. Za&nbsp;tydzień w&nbsp;niedzielę w&nbsp;naszym kościele
        parafialnym o&nbsp;godz.&nbsp;12.30 - Uroczysta Pierwsza
        Komunia&nbsp;św. dzieci z&nbsp;naszej parafii. Spowiedź&nbsp;św. dla
        dzieci pierwszokomunijnych i&nbsp;ich rodziców w&nbsp;najbliższy
        piątek o&nbsp;godz.&nbsp;16.00., a&nbsp;o&nbsp;16.30 próba,
        przygotowanie dzieci do&nbsp;Uroczystości Pierwszej
        Komunii&nbsp;Św.</p>
        <p>10. Nabożeństwo majowe przez cały ten miesiąc
        o&nbsp;godz.&nbsp;17.30. Zapraszamy wszystkich.</p>
        <p>11. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim życzymy błogosławionej
        Niedzieli</p>
