        <p>1. We&nbsp;wtorek przypada Dzień imienin księdza Biskupa
        Krzysztofa Nitkiewicza. Prosimy o&nbsp;dar modlitwy w&nbsp;intencji
        Czcigodnego Solenizanta.</p>
        <p>2. Przypominamy, że&nbsp;Wyższe Seminarium Duchowne
        w&nbsp;Sandomierzu przyjmuje zgłoszenia nowych kandydatów, którzy
        chcieliby rozpocząć formację i&nbsp;studia przygotowujące
        do&nbsp;sakramentu kapłaństwa. Chętni mogą zgłaszać się osobiście
