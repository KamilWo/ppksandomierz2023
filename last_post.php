        ul.&nbsp;Żeromskiego&nbsp;6. Możliwy jest także kontakt telefoniczny
        (tel.:&nbsp;511&nbsp;413&nbsp;002) lub mailowy
        (rozeznajpow@gmail.com).</p>
        <p>3. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma „Niedziela”, „Miłujcie się”, „Gość Niedzielny”
        i&nbsp;„Mały Gość Niedzielny”.</p>
        <p>4. W&nbsp;dniach od&nbsp;4 do&nbsp;12&nbsp;sierpnia&nbsp;br.
        odbędzie się XL&nbsp;Piesza Pielgrzymka Ziemi Sandomierskiej
        na&nbsp;Jasną Górę pod hasłem „Matko Kościoła prowadź. Więcej
        informacji na&nbsp;plakacie w&nbsp;gablocie przy głównym wejściu
        do&nbsp;kościoła. Zapraszamy na&nbsp;pątniczy szlak.</p>
        <p>5. Duszpasterstwo Służby Liturgicznej Diecezji Sandomierskiej
        zaprasza na&nbsp;Wakacyjną Szkołę Lektora połączoną
        z&nbsp;rekolekcjami. Propozycja skierowana jest do&nbsp;ministrantów,
        którzy ukończyli VII&nbsp;klasę i&nbsp;pragną posługiwać czytając
        Słowo Boże w&nbsp;czasie celebracji liturgicznych. Zapisy
        i&nbsp;szczegółowe informacje u&nbsp;księży wikariuszy.</p>
        <p>6. W&nbsp;niedzielę 6&nbsp;sierpnia w&nbsp;Zawichoście odbędą się
        Dni Młodych naszej Diecezji w&nbsp;łączności z&nbsp;Ojcem Świętym
        Franciszkiem, który w&nbsp;tym czasie będzie z&nbsp;młodymi
        z&nbsp;całego świata w&nbsp;Lizbonie. Zapraszamy młodzież
        do&nbsp;uczestnictwa w&nbsp;tym wydarzeniu. Szczegółowe informacje
        i&nbsp;zapisy u&nbsp;Księży wikariuszy.</p>
        <p>7. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim życzymy błogosławionej
        Niedzieli.</p>
