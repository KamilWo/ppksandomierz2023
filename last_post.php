    <div class="card-header">
        <h3>29.&nbsp;października 2023&nbsp;r.</h3>
        <h3>XXX&nbsp;Niedziela Zwykła, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. W&nbsp;zeszłą niedziele w&nbsp;naszej parafii liczyliśmy
        wiernych uczestniczących we&nbsp;Mszach św.(Dominicantes)
        i&nbsp;przyjmujących Komunie&nbsp;św.(Comunicantes). Takie
        są&nbsp;dane: we&nbsp;Mszach św. uczestniczyło 931&nbsp;osób
        (568&nbsp;kobiet i&nbsp;363&nbsp;mężczyzn), do&nbsp;Komunii św.
        przystąpiło 468&nbsp;osób (317&nbsp;kobiet
        i&nbsp;151&nbsp;mężczyzn).</p>
        <p>2. W&nbsp;najbliższą środę - <strong>1&nbsp;listopada</strong> -
        Uroczystość Wszystkich Świętych. W&nbsp;naszym parafialnym
        kościele porządek Mszy&nbsp;św. jak w&nbsp;niedzielę, z&nbsp;tym
        wyjątkiem, że&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;12.30 nie będzie.
        Na&nbsp;cmentarzu komunalnym Msza&nbsp;św. o&nbsp;godz.&nbsp;12.30,
        na&nbsp;cmentarzu katedralnym o&nbsp;godz.&nbsp;13.00
        i&nbsp;świętopawelskim Msza&nbsp;św. o&nbsp;godz.&nbsp;14.00.
        Przypominamy o&nbsp;możliwości zyskania odpustu zupełnego
        i&nbsp;ofiarowania go&nbsp;za&nbsp;zmarłych od&nbsp;1
        do&nbsp;8&nbsp;listopada. Warunkiem uzyskania odpustu jest
        nawiedzenie cmentarza lub kościoła w&nbsp;tym okresie, oraz
        wypełnienie zwykłych warunków, tj.&nbsp;przystąpienie do&nbsp;komunii
        św. w&nbsp;dniu nawiedzenia cmentarza lub kościoła i&nbsp;odmówienie
        modlitw "Ojcze nasz" i&nbsp;"Wierzę w&nbsp;Boga", i&nbsp;jakiejkolwiek
        modlitwy w&nbsp;intencjach wyznaczonych na&nbsp;dany dzień przez
        papieża.</p>
        <p>3. W&nbsp;dzień zaduszny 2&nbsp;listopada w&nbsp;naszym kościele
        będą sprawowane Msze&nbsp;św. o&nbsp;godz.&nbsp;6.30, następna
        za&nbsp;zmarłych polecanych w&nbsp;wypominkach z&nbsp;procesją
        po&nbsp;kościele i&nbsp;modlitwami za&nbsp;zmarłych
        o&nbsp;godz.&nbsp;9.00. O&nbsp;godz.&nbsp;17.30. Koronka
        do&nbsp;Bożego Miłosierdzia połączona z&nbsp;wypominkami
        i&nbsp;Msza&nbsp;św. o&nbsp;godz.&nbsp;18.00.</p>
        <p>4. W&nbsp;dniach od&nbsp;1 do&nbsp;8&nbsp;listopada
        godz.&nbsp;17.30 Koronka do&nbsp;Bożego Miłosierdzia
        z&nbsp;wypominkami za&nbsp;zmarłych.</p>
        <p>5. Rocznica śmierci Sługi Bożego Biskupa Piotra Gołębiowskiego
        przypada 2&nbsp;listopada – módlmy się o&nbsp;rychłą beatyfikację.</p>
        <p>6. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka dla
        dorosłych, młodzieży i&nbsp;dla dzieci. Zachęcamy do&nbsp;lektury
        prasy katolickiej.</p>
        <p>7. W&nbsp;tym tygodniu Pierwszy czwartek, piątek, sobota miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.30.</p>
        <p>8. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>9. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św., która sprawowana jest o&nbsp;godz.&nbsp;11.15 –
        adoracja Najśw. Sakramentu połączona z&nbsp;modlitwą różańcową, którą
        poprowadzi Koło Żywego Różańca z&nbsp;naszej parafii i&nbsp;zmiana
        tajemnic różańcowych. Zapraszamy do&nbsp;wspólnej modlitwy.</p>
        <p>10. Spotkanie dla kandydatów do&nbsp;bierzmowania
        z&nbsp;klas&nbsp;VI, VII i&nbsp;VIII we&nbsp;wtorek 7&nbsp;listopada
        po&nbsp;Mszy Świętej wieczorowej w&nbsp;kościele. Przypominamy,
        że&nbsp;z&nbsp;końcem października upływa termin złożenia deklaracji
        do&nbsp;bierzmowania. Kto jeszcze deklaracji nie złożył, prosimy aby
        to&nbsp;uczynił w&nbsp;najbliższym czasie. Najbliższe spotkanie
        poprowadzi młodzież z&nbsp;Katolickiego Stowarzyszenia Młodzieży
        z&nbsp;diecezji.</p>
        <p>11. Wszystkich chłopców, którzy chcieliby zostać ministrantami
        i&nbsp;służyć przy ołtarzu oraz dziewczynki, które chciałby śpiewać
        w&nbsp;scholi, prosimy o&nbsp;kontakt z&nbsp;księżmi wikariuszami.
        Zwracamy się także do&nbsp;rodziców z&nbsp;prośbą, by&nbsp;zachęcali
        i&nbsp;umożliwili dzieciom uczęszczanie na&nbsp;spotkaniach
        organizowane przy parafii. Za&nbsp;wszelką pomoc – dziękujemy.</p>
        <p>12. Wszystkie dzieci zachęcamy do&nbsp;wzięcia udziału
        w&nbsp;korowodzie świętych, który w&nbsp;naszej parafii odbędzie się
        w&nbsp;niedziele 5&nbsp;listopada. Zachęcamy, aby na&nbsp;ten dzień
        dzieci przyszły do&nbsp;kościoła przebrane za&nbsp;świętego.
        Rozpoczęcie korowodu o&nbsp;godz.&nbsp;12.15 przy figurze Matki Bożej
        przed kościołem i&nbsp;następnie Msza Świętą
        o&nbsp;godz.&nbsp;12.30.</p>
        <p>13. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
