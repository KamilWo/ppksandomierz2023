        <h3>22.&nbsp;października 2023&nbsp;r.</h3>
        <h3>XXIX&nbsp;Niedziela Zwykła, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. W&nbsp;minioną niedzielę do&nbsp;puszek na&nbsp;wsparcie Dzieła
        Nowego Tysiąclecia – stypendia dla zdolnej a&nbsp;ubogiej młodzieży
        zebrane zostało 3758&nbsp;zł. <em>Bóg zapłać</em> wszystkim
        ofiarodawcom. Pieniądze zostały przekazane na&nbsp;wspomniany cel.</p>
        <p>2. Nabożeństwo różańcowe w&nbsp;październiku o&nbsp;godzinie 17.30
        w&nbsp;dni powszednie i&nbsp;w&nbsp;niedzielę. Zapraszamy
        do&nbsp;wzięcia udziału w&nbsp;tej pięknej modlitwie.</p>
        <p>3. Dziś w&nbsp;naszej parafii liczymy wiernych uczestniczących
        we&nbsp;Mszach&nbsp;św. (Dominicantes) i&nbsp;przyjmujących
        Komunie&nbsp;św. (Comunicantes). Obliczenia przekażemy w&nbsp;przyszłą
        niedzielę.</p>
        <p>4. Ta&nbsp;niedziela jest tzw.&nbsp;<strong>Niedzielą
        Misyjną</strong> i&nbsp;rozpoczyna <strong>Tydzień Misyjny</strong>,
        który ma&nbsp;na&nbsp;celu gorącą modlitwę i&nbsp;troskę
        o&nbsp;zrozumienie przez wiernych potrzeb misyjnych. Na&nbsp;różańcu
        będziemy się modlić w&nbsp;intencji misjonarzy, zwłaszcza
        za&nbsp;tych, pochodzących z&nbsp;naszej diecezji.</p>
        <p>5. W&nbsp;najbliższa sobotę o&nbsp;godz.&nbsp;18.00 w&nbsp;naszym
        parafialnym kościele odbędzie się kolejny wieczór Uwielbienia.
        Zapraszamy.</p>
        <p>6. W&nbsp;przyszłą niedzielę na&nbsp;Mszy Świętej
        o&nbsp;godz.&nbsp;12.30 rozstrzygnięcie konkursu plastycznego dla
        dzieci.</p>
        <p>7. Spotkanie dla kandydatów do&nbsp;bierzmowania z&nbsp;klas VI,
        VII i&nbsp;VIII we&nbsp;wtorek 7&nbsp;listopada po&nbsp;Mszy Świętej
        wieczorowej w&nbsp;kościele. Przypominamy, że&nbsp;z&nbsp;końcem
        października upływa termin złożenia deklaracji do&nbsp;bierzmowania.
        Kto jeszcze deklaracji nie złożył, prosimy aby to&nbsp;uczynił
        w&nbsp;najbliższym czasie.</p>
        <p>8. Zachęcamy wszystkich rodziców, rodziców chrzestnych, osoby,
        które straciły dziecko do&nbsp;włączenia się w&nbsp;Różaniec rodziców
        za&nbsp;dzieci. Kilka osób już się zgłosiło. Szczegółowe informacje
        u&nbsp;ks.&nbsp;Krystiana Musiała.</p>
        <p>9. Wszystkie dzieci zachęcamy do&nbsp;wzięcia udziału
        w&nbsp;korowodzie świętych, który w&nbsp;naszej parafii odbędzie się
        w&nbsp;niedziele 5&nbsp;listopada. Zachęcamy aby na&nbsp;ten dzień
        dzieci przyszły do&nbsp;kościoła przebrane za&nbsp;świętego.
        Szczegółowe informacje zostaną podane w&nbsp;przyszła niedzielę.</p>
        <p>10. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;lektury prasy katolickiej.</p>
        <p>11. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, złożone ofiary na&nbsp;rzecz parafii.
        Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
