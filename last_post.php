        <p>1. Dziś po&nbsp;Mszach&nbsp;św. zbiórka do&nbsp;puszek
        na&nbsp;wsparcie Dzieła Nowego Tysiąclecia – stypendia dla zdolnej
        a&nbsp;ubogiej młodzieży.</p>
        <p>2. Kartki na&nbsp;wypominki na&nbsp;stoliku obok gazet.</p>
        <p>3. W&nbsp;najbliższy wtorek, jak w&nbsp;każdy trzeci wtorek
        miesiąca w&nbsp;tym roku, zapraszamy na&nbsp;kolejne spotkanie
        Akademii Biblijnej w&nbsp;Sandomierzu; poprowadzi je&nbsp;wykładowca
        Pisma świętego w&nbsp;naszym seminarium, ks.&nbsp;Paweł Lasek.
        Tematem tego spotkania będzie: Zdobycie Jerycha i&nbsp;Kanaanu przez
        Jozuego. Spotkanie będzie we&nbsp;wtorek, 17&nbsp;października
        o&nbsp;godz.&nbsp;18.45, w&nbsp;auli Wyższego Seminarium Duchownego.
        Serdecznie zapraszamy!</p>
        <p>4. Zapraszamy na&nbsp;wykład otwarty z&nbsp;filozofii wraz
        prezentacja pt. „Sztuczna Inteligencja szansa czy zagrożenie?” wykład
        odbędzie się w&nbsp;sobotę 21&nbsp;października br.
        o&nbsp;godz.&nbsp;16.00 w&nbsp;domu bł.&nbsp;Antoniego Rewery przy
        kościele św.&nbsp;Józefa w&nbsp;Sandomierzu. Szczegóły
        na&nbsp;plakacie w&nbsp;gablocie.</p>
        <p>5. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>6. W&nbsp;przyszłą niedzielę liczenie wiernych biorących udział
        we&nbsp;Mszach&nbsp;św. i&nbsp;przyjmujących Komunię&nbsp;Św.</p>
        <p>7. Apostolat Margaretka Diecezji Sandomierskiej zaprasza
        na&nbsp;Różaniec w&nbsp;intencji kapłanów, który odbędzie się
        w&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.30 w&nbsp;naszym
        kościele.</p>
        <p>8. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim życzymy błogosławionej
        Niedzieli.</p>
