        <h3>24.&nbsp;grudnia 2023&nbsp;r.</h3>
        <h3>IV&nbsp;Niedziela Adwentu, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Zachęcamy do&nbsp;zachowania tradycji podczas wieczerzy
        wigilijnej, w&nbsp;czasie której powinno mieć miejsce czytanie
        fragmentu Ewangelii św.&nbsp;Łukasza o&nbsp;narodzeniu Pana Jezusa,
        modlitwa rodzinna, śpiewanie kolęd i&nbsp;dania bezmięsne.</p>
        <p>2. W&nbsp;Boże Narodzenie Msza&nbsp;św. Pasterka o&nbsp;północy.
        Taca z&nbsp;Pasterki przeznaczona jest na&nbsp;Diecezjalny „Fundusz
        Ochrony Życia”, a&nbsp;taca z&nbsp;przyszłej niedzieli na&nbsp;Wyższe
        Seminarium Duchowne w&nbsp;Sandomierzu. W&nbsp;Boże Narodzenie
        porządek Mszy&nbsp;św. jak w&nbsp;niedzielę. W&nbsp;Święto św.
        Szczepana nie ma&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;18.00.</p>
        <p>3. Słowa wdzięczności kierujemy do&nbsp;parafian, którzy
        przygotowali Bożonarodzeniową szopkę w&nbsp;naszym parafialnym
        kościele. Dziękujemy za&nbsp;wykonanie dekoracji Bożonarodzeniowej
        przy ołtarzu, oprawienie choinek. <em>Bóg zapłać</em> wszystkim,
        którzy w&nbsp;jakikolwiek sposób angażują się w&nbsp;parafialne
        duszpasterstwo.</p>
        <p>4. <strong>Wizyta duszpasterska</strong></p>
        <p><strong>Czwartek – Chwałki od&nbsp;godz.&nbsp;9.00</strong></p>
        <p>I&nbsp;Ksiądz – od&nbsp;drogi na&nbsp;Sucharzów</p>
        <p>II&nbsp;Ksiądz – od&nbsp;początku od&nbsp;ul.&nbsp;Lipowej</p>
        <p>III&nbsp;Ksiądz – od&nbsp;marketu Kaufland</p>
        <p>&nbsp;</p>
        <p><strong>Piątek – Milczany od&nbsp;godz.&nbsp;9.00</strong>
        <p>I&nbsp;Ksiądz – Milczany po&nbsp;szosie</p>
        <p>II&nbsp;Ksiądz – Milczany od&nbsp;końca</p>
        <p>III&nbsp;Ksiądz – Milczany Wieś</p>
        <p>&nbsp;</p>
        <p><strong>Sobota – Kobierniki od&nbsp;godz.&nbsp;9.00</strong>
        <p>I&nbsp;Ksiądz – Kobierniki Górne od&nbsp;ul.&nbsp;Mickiewicza</p>
        <p>II&nbsp;Ksiądz – Kobierniki Górne od&nbsp;ul.&nbsp;Polnej</p>
        <p>III&nbsp;Ksiądz – Kobierniki po&nbsp;szosie i&nbsp;Wieś</p>
        <p>&nbsp;</p>
        <p>5. Zapraszamy na&nbsp;kolędowy Wieczór Uwielbienia
        w&nbsp;najbliższą sobotę o&nbsp;godzinie 18.00.</p>
        <p>6. W&nbsp;niedzielę <strong>31&nbsp;grudnia</strong> na&nbsp;Mszy świętej
        o&nbsp;godz.&nbsp;12.30 odbędzie się rozpoczęcie „Różańca rodziców
        za&nbsp;dzieci”, osoby chętne, które chcą jeszcze dołączyć
        do&nbsp;tej grupy prosimy o&nbsp;kontakt z&nbsp;ks.&nbsp;Krystianem
        Musiałem.</p>
        <p>7. Zbiórki dla lektorów, ministrantów i&nbsp;kandydatów
        w&nbsp;miesiącu styczniu odbywać się będą w&nbsp;niedzielę przed
        Mszą świętą o&nbsp;godz.&nbsp;11.15 (początek
        o&nbsp;godz.&nbsp;10.40).</p>
        <p>8. Zachęcamy do&nbsp;czytania prasy katolickiej; „Gość Niedzielny”,
        „Niedziela”, są&nbsp;do&nbsp;nabycia na&nbsp;stoliku pod chórem
        w&nbsp;kościele.</p>
        <p>9. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, złożone ofiary na&nbsp;rzecz parafii –
        <em>Bóg zapłać</em>. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św.
        życzymy błogosławionej niedzieli.</p>
