        <h3>26.&nbsp;lutego 2023&nbsp;r.</h3>
        <h3>I&nbsp;Niedziela Wielkiego Postu, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. Słowa wdzięczności kierujemy do&nbsp;naszej parafianki,
        za&nbsp;wykonanie wielkopostnej dekoracji w&nbsp;kościele. <em>Bóg
        zapłać</em>.</p>
        <p>2. Droga Krzyżowa w&nbsp;każdy piątek Wielkiego Postu
        o&nbsp;godz.&nbsp;17.15 - zapraszamy do&nbsp;udziału. W&nbsp;każdą
        niedzielę Wielkiego Postu nabożeństwo „Gorzkich Żali” z&nbsp;nauką
        pasyjną o&nbsp;godz.&nbsp;17.30, następnie Msza Święta. Za&nbsp;udział
        w&nbsp;tych nabożeństwach, można zyskać odpust zupełny pod zwykłymi
        warunkami.</p>
        <p>3. Z&nbsp;niedzieli na&nbsp;poniedziałek, tj.&nbsp;12
        na&nbsp;13&nbsp;marca&nbsp;br. dekanat sandomierski ma&nbsp;czuwanie
        modlitewne na&nbsp;Jasnej Górze przed wizerunkiem Czarnej Madonny
        w&nbsp;intencji powołań w&nbsp;Kościele Sandomierskim. Wyjazd
        autokarem w&nbsp;niedzielę o&nbsp;godz.&nbsp;15.00 powrót nocą.
        Prosimy o&nbsp;zgłoszenie się chętnych do&nbsp;wyjazdu. Zapisujemy
        w&nbsp;zakrystii.</p>
        <p>4. Zachęcamy do&nbsp;nabycia i&nbsp;lektury katolickiej prasy.
        Gazety na&nbsp;stoliku pod chórem.</p>
        <p>5. Kurs Przedmałżeński w&nbsp;dekanacie sandomierskim, dziś
        rozpoczyna się w&nbsp;parafii Chrystusa Króla Jedynego Zbawiciela
        Świata u&nbsp;OO.&nbsp;Pallotynów w&nbsp;Sandomierzu,
        o&nbsp;godz.&nbsp;15.00, spotkanie na&nbsp;plebanii.</p>
        <p>6. W&nbsp;tym tygodniu Pierwszy czwartek, piątek i&nbsp;Pierwsza
        sobota miesiąca. W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym
        dniu od&nbsp;godz.&nbsp;17.00. W&nbsp;Pierwszy czwartek, Legion Maryi
        poprowadzi „Godzinę Świętą”. Jest nabożeństwo wynagradzające Najśw.
        Sercu Pana Jezusa przed wystawionym Najśw. Sakramentem, początek
        nabożeństwa o&nbsp;godz.&nbsp;17.00. <strong>W&nbsp;pierwszy piątek
        o&nbsp;godz.&nbsp;16.30 spotkanie młodzieży przygotowującej się
        do&nbsp;sakramentu bierzmowania z&nbsp;klas VIII.</strong> Zapraszamy.</p>
        <p>7. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>8. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św. o&nbsp;godz.&nbsp;11.15 – adoracja Najśw. Sakramentu
        połączona z&nbsp;modlitwą różańcową, którą poprowadzi Koło Żywego
        Różańca z&nbsp;naszej parafii i&nbsp;zmiana tajemnic różańcowych.
        Zapraszamy do&nbsp;udziału w&nbsp;tej modlitwie.</p>
        <p>9. Przypominamy, że&nbsp;trwają katechezy dla poszukujących
        głębszego zrozumienia chrześcijańskiej wiary. W&nbsp;każdy
        poniedziałek i&nbsp;piętek Wielkiego Postu o&nbsp;godz.&nbsp;19.15
        w&nbsp;sali przy parafii św.&nbsp;Józefa. Zaprasza Wspólnota
        Neokatechumenalna".</p>
        <p>10. W&nbsp;ramach jałmużny wielkopostnej, w&nbsp;przyszłą
        niedzielę zbiórka do&nbsp;puszek na&nbsp;rzecz Dzieła Pomocy
        misjonarzom „Ad&nbsp;Gentes”, „Do&nbsp;narodów”. Zadaniem tego dzieła
        jest wspieranie działalności na&nbsp;rzecz rozwoju -
        w&nbsp;szczególności edukacyjnej, w&nbsp;zakresie ochrony zdrowia,
        służącej redukcji ubóstwa, społecznej i&nbsp;kulturowej
        na&nbsp;terenach misyjnych, prowadzonej przez polskich misjonarzy.</p>
        <p>11. Dziękujemy za&nbsp;modlitwę, wszelką pomoc, składane ofiary
        na&nbsp;potrzeby parafii. Wszystkim, którzy w&nbsp;tym tygodniu
        obchodzą swoje imieniny, urodziny, inne rocznice, składamy najlepsze
        życzenia z&nbsp;kapłańskim błogosławieństwem. Wszystkim
        uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
        <p>
