        w&nbsp;ciągu całego roku. Zapraszamy dzieci, młodzież, rodziców,
        nauczycieli do&nbsp;udziału we&nbsp;Mszy Świętej. Okazja
        do&nbsp;spowiedzi codziennie wieczorem przed Mszą Świętą.</p>
        <p>3. W&nbsp;piątek, tj.&nbsp;23&nbsp;czerwca obchodzimy Dzień Ojca.
        Polecajmy Panu Bogu wszystkich ojców.</p>
        <p>4. W&nbsp;najbliższą sobotę, tj.&nbsp;24&nbsp;czerwca Uroczystość
        Narodzenia św.&nbsp;Jana Chrzciciela, a&nbsp;także w&nbsp;naszej
        parafii w&nbsp;tym dniu odbędzie się ostatni przed przerwą wakacyjną
        Wieczór Uwielbienia. Rozpoczęcie Mszą&nbsp;Św.
        o&nbsp;godz.&nbsp;18.00, a&nbsp;po&nbsp;niej Adoracja Pana Jezusa
        w&nbsp;Najświętszym Sakramencie. Tym razem naszej modlitwie będzie
        towarzyszyła „Wspólnota przez Jej serce” z&nbsp;Sulisławic. Serdecznie
        zapraszamy.</p>
        <p>5. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
