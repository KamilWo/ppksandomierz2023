        <h3>3.&nbsp;grudnia 2023&nbsp;r.</h3>
        <h3>I&nbsp;Niedziela Adwentu, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Dla ludzi wierzących Adwent jest czasem szczególnym. Jest
        czasem oczekiwania z&nbsp;wiarą na&nbsp;powtórne przyjście Zbawiciela
        na&nbsp;ziemię, a&nbsp;jednocześnie przygotowania się do&nbsp;obchodów
        Uroczystości Bożego Narodzenia. Zachęcamy parafian
        do&nbsp;uczestnictwa w&nbsp;Mszach&nbsp;św. Roratnich
        i&nbsp;do&nbsp;spotkania z&nbsp;przebaczającym Jezusem
        w&nbsp;Sakramencie Pokuty. Msza&nbsp;św. Roratnia w&nbsp;naszym
        kościele, w&nbsp;dni powszednie, w&nbsp;okresie Adwentu
        o&nbsp;godz.&nbsp;6.30., a&nbsp;przed Nią o&nbsp;godz.&nbsp;6.10 będą
        śpiewane Godzinki. Zapraszamy</p>
        <p>2. Wszystkie dzieci zapraszamy na&nbsp;<em>Czuwanie
        ze&nbsp;św.&nbsp;Franciszkiem</em>. Przez cały adwent (od&nbsp;poniedziałku
        do&nbsp;soboty) na&nbsp;Mszy Świętej o&nbsp;godz.&nbsp;18.00 będziemy
        razem ze&nbsp;św.&nbsp;Franciszkiem przygotowywać się
        na&nbsp;przyjście Pana Jezusa. Zachęcamy dzieci, aby każdego dnia
        przygotowały serduszko z&nbsp;dobrym uczynkiem. Prosimy, aby dzieci
        na&nbsp;Msze święte wieczorowe przynosiły lampiony. Zwracamy się
        z&nbsp;prośbą także do&nbsp;rodziców, aby zachęcali swoje dzieci
        do&nbsp;uczestnictwa w&nbsp;tych nabożeństwach i&nbsp;pomogły
        dzieciom w&nbsp;dotarciu do&nbsp;kościoła.</p>
        <p>3. Od&nbsp;dziś można nabyć świece wigilijną „Caritas” – Wigilijne
        Dzieło Pomocy Dzieciom - jak to&nbsp;było w&nbsp;poprzednich latach.
        Mała świeca - 10&nbsp;zł, duża - 25&nbsp;zł. Są&nbsp;w&nbsp;zakrystii.
        Poświęcone Opłatki Wigilijne też można nabyć w&nbsp;zakrystii.
        Zapraszamy.</p>
        <p>4. II&nbsp;niedziela Adwentu będzie dniem modlitwy i&nbsp;pomocy
        materialnej Kościołowi na&nbsp;Wschodzie – po&nbsp;Mszach&nbsp;św.
        zbiórka do&nbsp;puszek na&nbsp;wsparcie Kościoła na&nbsp;Wschodzie.</p>
        <p>5. Uroczystość Niepokalanego Poczęcia w&nbsp;Kościele Powszechnym
        obchodzimy 8&nbsp;grudnia, tj.&nbsp;w&nbsp;najbliższy piątek.
        W&nbsp;tym dniu na&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;18.00 zostaną
        poświęcone medaliki dla dzieci przygotowujące się do&nbsp;Pierwszej
        Komunii&nbsp;Św. A&nbsp;po&nbsp;Mszy&nbsp;św. spotkanie
        z&nbsp;rodzicami pierwszokomunijnych dzieci. Prosimy rodziców tych
        dzieci o&nbsp;zakupienie medalików dla swoich pociech.</p>
        <p>6. Zachęcamy do&nbsp;czytania prasy katolickiej; „Gość Niedzielny”,
        „Niedziela”, dla młodzieży dwutygodnik „Miłujcie się”.
        Są&nbsp;do&nbsp;nabycia na&nbsp;stoliku pod chórem w&nbsp;kościele.</p>
        <p>7. Fundacja Małych Stópek, zaprasza do&nbsp;udziału
        w&nbsp;świątecznej zbiórce artykułów higienicznych „Paczuszka dla
        Maluszka”, dla dzieci w&nbsp;domach samotnej matki, domach dziecka
        i&nbsp;hospicjach w&nbsp;całej Polsce. W&nbsp;inicjatywę można włączyć
        się przygotowując paczkę z&nbsp;artykułami takimi, jak: kremy,
        szampony, mydła, żele do&nbsp;kąpieli, proszki i&nbsp;płyny
        do&nbsp;prania oraz art. chemii gospodarczej i&nbsp;zostawiając
        je&nbsp;pod choinką przy kaplicy MB&nbsp;w&nbsp;naszym kościele.</p>
        <p>8. Duszpasterstwo Kobiet Diecezji Sandomierskiej i&nbsp;Parafia
        Katedralna w&nbsp;Sandomierzu zapraszają 8&nbsp;grudnia&nbsp;br.
        do&nbsp;Bazyliki katedralnej wszystkie Kobiety na&nbsp;wspólną
        modlitwę w&nbsp;ich intencji. Jest to&nbsp;Ogólnopolska Akcja
        Modlitewna pt.&nbsp;„Wpatrzeni w&nbsp;Niepokalaną”. Spotkanie
        rozpocznie się adoracją Najśw. Sakramentu o&nbsp;godz.&nbsp;17.00,
        której tematyka będzie miała charakter maryjny, a&nbsp;po&nbsp;niej
        będzie Msza&nbsp;św., podczas której dokona się obrzęd Zawierzenia
        się Jezusowi w&nbsp;niewolę miłości przez ręce Maryi.</p>
        <p>9. Diecezjalny Duszpasterz Osób Żyjących w&nbsp;związkach
        Niesakramentalnych, zaprasza osoby w&nbsp;takich związkach, jak
        również tych, którzy żyją w&nbsp;separacji i&nbsp;rozwiedzionych,
        którzy nie zawarli nowego związku na&nbsp;Adwentowy Dzień Skupienia
        w&nbsp;dniu 9&nbsp;grudnia 2023&nbsp;r. na&nbsp;godz.&nbsp;9.30
        do&nbsp;instytutu Teologicznego w&nbsp;Sandomierzu
        ul.&nbsp;Mariacka&nbsp;9.</p>
        <p>10. W&nbsp;przyszłym roku planowana jest pielgrzymka do&nbsp;Włoch.
        Więcej informacji podamy w&nbsp;najbliższym czasie. Szczegółowe
        informacje u&nbsp;ks.&nbsp;Krystiana Musiała.</p>
        <p>11. Dziękujemy parafianom, którzy przygotowały dekoracje
        na&nbsp;Adwent.</p>
        <p>12. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, inne rocznice, składamy najlepsze życzenia z&nbsp;kapłańskim
        błogosławieństwem. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św.
        życzymy błogosławionej niedzieli.</p>
