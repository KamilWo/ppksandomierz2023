    <div class="col">
        <p>1. Przypominamy, że&nbsp;Wyższe Seminarium Duchowne
        w&nbsp;Sandomierzu przyjmuje zgłoszenia nowych kandydatów, którzy
        chcieliby rozpocząć formację i&nbsp;studia przygotowujące
        do&nbsp;sakramentu kapłaństwa. Chętni mogą zgłaszać się osobiście
        w&nbsp;siedzibie Seminarium Duchownego w&nbsp;Sandomierzu,
        ul.&nbsp;Żeromskiego&nbsp;6. Możliwy jest także kontakt telefoniczny
        (tel.:&nbsp;511&nbsp;413&nbsp;002) lub mailowy
        (rozeznajpow@gmail.com).</p>
        <p>2. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma „Niedziela”, „Miłujcie się”, „Gość Niedzielny”
        i&nbsp;„Mały Gość Niedzielny”. <strong>Słabo jest z&nbsp;czytaniem
        prasy katolickiej w&nbsp;naszej parafii. Zamawiamy 15&nbsp;egzemplarzy
        gazet na&nbsp;tydzień i&nbsp;więcej niż połowę zamówionych
        zwracamy.</strong></p>
        <p>3. W&nbsp;dniach od&nbsp;4 do&nbsp;12&nbsp;sierpnia br. odbędzie
        się XL&nbsp;Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę
        pod hasłem „Matko Kościoła prowadź”. Pielgrzymka ma&nbsp;charakter
        wędrownych rekolekcji z&nbsp;programem różnych modlitw, konferencji
        i&nbsp;śpiewu. Jest zarazem jedną z&nbsp;dróg prowadzących
        do&nbsp;Pana Boga poprzez wstawiennictwo Maryi. Zapraszamy
        na&nbsp;pątniczy szlak.</p>
        <p>4. Duszpasterstwo Służby Liturgicznej Diecezji Sandomierskiej
        zaprasza na&nbsp;Wakacyjną Szkołę Lektora połączoną
        z&nbsp;rekolekcjami. Propozycja skierowana jest do&nbsp;ministrantów,
        którzy ukończyli VII&nbsp;klasę i&nbsp;pragną posługiwać czytając
        Słowo Boże w&nbsp;czasie celebracji liturgicznych. Szkoła obejmuje
        zajęcia fonetyczne, biblijne i&nbsp;liturgiczne poszerzone
        o&nbsp;formację duchową w&nbsp;formie warsztatowych rekolekcji.
        Na&nbsp;zakończenie kursu nowi lektorzy otrzymają błogosławieństwo
        do&nbsp;posługi.<br />
        W&nbsp;miarę możliwości zapraszamy także lektorów, którzy już pełnią
        posługę w&nbsp;parafiach chcieliby pogłębić swoją formację.</p>
        <p>5. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim życzymy błogosławionej
        Niedzieli.</p>
