        w&nbsp;Syrii i&nbsp;Turcji. W&nbsp;przyszłym terminie zorganizujemy
        zbiórkę dla chorych księży.</p>
        <p>2. W&nbsp;najbliższy piątek, jak w&nbsp;każdy trzeci piątek
