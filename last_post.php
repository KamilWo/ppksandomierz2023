        <h3>02.&nbsp;lipca 2023&nbsp;r.</h3>
        <h3>XIII&nbsp;Niedziela Zwykła, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. Ks.&nbsp;Biskup Ordynariusz zamianował ks.&nbsp;Krystiana
        Musiała wikariuszem w&nbsp;naszej parafii. Ks.&nbsp;Krystian Musiał
        uprzednio pracował w&nbsp;parafii św.&nbsp;Bartłomieja
        w&nbsp;Staszowie. Życzymy nowemu Ks.&nbsp;Wikariuszowi wszystkie
        dobrego w&nbsp;posługiwaniu w&nbsp;naszej parafii.
        Zaś&nbsp;ks.&nbsp;Krzysztof Rębisz został skierowany do&nbsp;parafii
        w&nbsp;Szewnej k/Ostrowca&nbsp;św. Dziękujemy Mu&nbsp;za&nbsp;posługę
        w&nbsp;naszej parafii i&nbsp;życzymy zdrowia, i&nbsp;wszelkich łask
        w&nbsp;kapłańskim życiu.</p>
        <p>2. We&nbsp;wtorek, tj.&nbsp;4&nbsp;lipca przypada Rocznica sakry
        i&nbsp;ingresu Ks.&nbsp;Bp.&nbsp;Krzysztofa Nitkiewicza, w&nbsp;naszej
        modlitwie liturgicznej i&nbsp;osobistej polecajmy Panu Bogu Pasterza
        Kościoła Sandomierskiego.</p>
        <p>3. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma „Niedziela”, „Miłujcie się”, „Gość Niedzielny”
        i&nbsp;„Mały Gość Niedzielny”.</p>
        <p>4. W&nbsp;dniach od&nbsp;4 do&nbsp;12&nbsp;sierpnia br. odbędzie
        się XL&nbsp;Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę
        pod hasłem „Matko Kościoła prowadź”, Pielgrzymka ma&nbsp;charakter
        wędrownych rekolekcji z&nbsp;programem różnych modlitw, konferencji
        i&nbsp;śpiewu. Jest zarazem jedną z&nbsp;dróg prowadzących
        do&nbsp;Pana Boga poprzez wstawiennictwo Maryi. Na&nbsp;pątniczy szlak
        zapraszamy ludzi dobrej woli. Więcej informacji na&nbsp;ten temat
        w&nbsp;ogłoszeniach parafialnych w&nbsp;kolejne niedziele.</p>
        <p>5. W&nbsp;tym tygodniu Pierwszy czwartek i&nbsp;piątek miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.30.</p>
        <p>6. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, inne rocznice, składamy najlepsze życzenia. Wszystkim
        uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
