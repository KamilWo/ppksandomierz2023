        <h3>1.&nbsp;stycznia 2023&nbsp;r.</h3>
        <h3>Uroczystość Świętej Bożej Rodzicielki – Nowy Rok, Rok&nbsp;A</h3>
    </div>
    <div class="col">
        <p>1. Dziękujemy wszystkim tym rodzinom z&nbsp;naszej parafii, które
        w&nbsp;minionym tygodniu przyjęły nas kapłanów z&nbsp;wizyta
        duszpasterską. Niech to&nbsp;błogosławieństwo Boże, które
        pozostawiliśmy w&nbsp;Waszych domach, napełni Was i&nbsp;wasze rodziny
        pokojem i&nbsp;radością, umocni wiarę i&nbsp;pomaga z&nbsp;ufnością
        pokładaną Bogu przezwyciężać różne problemy codziennego życia.
        Serdeczne: <em>Bóg zapłać</em>.</p>
        <p>2. W&nbsp;najbliższy piątek – Uroczystość Objawienia Pańskiego –
        w&nbsp;tradycji: Święto Trzech Króli – prządek Mszy&nbsp;św., jak
        w&nbsp;niedzielę. Poświęcenie kredy i&nbsp;kadzidła
        na&nbsp;Mszach&nbsp;św. Poświęconą kredą oznaczamy odrzwia naszych
        domów i&nbsp;mieszkań <strong>C+M+B 2023</strong>.
        O&nbsp;godz.&nbsp;12.00 w&nbsp;tym dniu wyruszy od&nbsp;naszego
        kościoła orszak z&nbsp;jednym z&nbsp;„Trzech Króli”. Wspólnie udamy
        się ze&nbsp;śpiewem kolęd do&nbsp;stajenki na&nbsp;rynku Starego
        Miasta, aby złożyć pokłon Nowonarodzonemu Królowi Świata. Zakończenie
        uroczystości o&nbsp;godz.&nbsp;13.30. Będzie gorąca herbata
        i&nbsp;pączek. Zapraszamy wszystkich, dorosłych, młodzież
        i&nbsp;dzieci!</p>
        <p>3. <strong>Prządek Wizyty Duszpasterskiej</strong> – zakładka
        na&nbsp;stronie.</p>
        <p>4. W&nbsp;tym tygodniu Pierwszy czwartek, piątek miesiąca.
        W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.30.</p>
        <p>5. Koncert kolęd Chóru Katedralnego dziś o&nbsp;godz.&nbsp;16.00
        w&nbsp;katedrze Sandomierskiej. Zapraszamy.</p>
        <p>6. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>7. W&nbsp;przyszłą niedzielę – Święto Chrztu Pańskiego. Koniec
        Okresu Bożego Narodzenia. Tradycyjnie bożonarodzeniową dekorację
        zachowujemy w&nbsp;kościele do&nbsp;Święta Ofiarowania Pańskiego –
        tradycyjnie – Matki Bożej Gromnicznej.</p>
        <p>8. Wdzięczni jesteśmy za&nbsp;modlitwę, dobre słowo, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim, którzy w&nbsp;tym
        tygodniu obchodzą swoje imieniny, urodziny, inne rocznice, składamy
        najlepsze życzenia z&nbsp;kapłańskim błogosławieństwem. Wszystkim
        uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej niedzieli
        i&nbsp;Szczęśliwego Nowego Roku.</p>
